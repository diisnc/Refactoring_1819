    class monotorizaPreco extends Thread {
        
        public void run(){
            while(executar == true){
                ativo = tc.getAtivo(ativo.getIdAtivo());
                if(jRadioButton1.isSelected()) {
                    jLabel6.setText(Float.toString(ativo.getPrecoVenda()));
                } else {
                    jLabel6.setText(Float.toString(ativo.getPrecoCompra()));
                }
                try{
                    Thread.sleep(1000);
                } catch (InterruptedException ex) {
                    ex.printStackTrace();
                }
            }
            
        }
    }
