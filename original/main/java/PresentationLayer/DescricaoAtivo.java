    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        Byte categoria = 0 ;
        float precoAbertura =ativo.getPrecoCompra();
        float nUnidades,valorInvestido;
        if (Float.valueOf(Amount.getText()) > tc.getAtualUser().getConta().getSaldo()) {
            JOptionPane.showMessageDialog(this, "Saldo Insuficiente! Saldo: " + tc.getAtualUser().getConta().getSaldo(), "Atenção", ERROR_MESSAGE);
            return;
        }
        if (Float.valueOf(Stop_Loss.getText()) > Float.valueOf(Amount.getText())) {
            JOptionPane.showMessageDialog(this, "Stop Loss não pode ser inferior ao valor investido", "Atenção", ERROR_MESSAGE);
            return;
        }
        if (Float.valueOf(Take_Profit.getText()) > Float.valueOf(Amount.getText()) * 100) {
            JOptionPane.showMessageDialog(this, "Take Profit não pode ser 100% superior ao valor investido", "Atenção", ERROR_MESSAGE);
            return;
        }
        if (jButton1.getText().equals("Vender")) {
            categoria = 1;
            precoAbertura = ativo.getPrecoVenda();
        }
        if(jToggleButton1.getText().equals("Units")) {
            valorInvestido = Float.parseFloat(Amount.getText());
            nUnidades = valorInvestido /precoAbertura;
            nUnidades = TradingCore.round(nUnidades,2).floatValue();
        }else{
            nUnidades = Float.parseFloat(Amount.getText());
            valorInvestido = nUnidades*precoAbertura;
            valorInvestido = TradingCore.round(valorInvestido,2).floatValue();
        }
        CFD cfd = new CFD (ativo.getIdAtivo(),precoAbertura,Float.parseFloat(Take_Profit.getText()),
                            Float.parseFloat(Stop_Loss.getText()),categoria,
                            valorInvestido,nUnidades,tc.getAtualUser().getUsername());
        
        tc.insereCFD(cfd);
        this.executar = false;
        try {
            this.mP.join();
        } catch (InterruptedException ex) {
            ex.printStackTrace();
        }
        dispose();
    }//GEN-LAST:event_jButton1MouseClicked

