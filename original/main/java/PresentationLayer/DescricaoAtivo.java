    private void jToggleButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jToggleButton1MousePressed
        float valorTipo = 0;
        if (jRadioButton1.isSelected()==true ) valorTipo = ativo.getPrecoVenda();
        else valorTipo =  ativo.getPrecoCompra();
        if (jToggleButton1.isSelected()){
            jToggleButton1.setText("Units");
            jLabel3.setText("Amount");
            jLabel4.setText("€");
            if(Amount.getText().equals("")==false){
               float uni = Float.valueOf(Amount.getText());
               float valor = uni * valorTipo;
               valor = TradingCore.round(valor,2).floatValue();
               Amount.setText(Float.toString(valor));
            }
        } else {
            jToggleButton1.setText("Amount");
            jLabel3.setText("Units");
            jLabel4.setText("uni");
            if(Amount.getText().equals("")==false){
                float valor = Float.valueOf(Amount.getText());
                float uni = valor / valorTipo;
                uni = TradingCore.round(uni,2).floatValue();
                Amount.setText(Float.toString(uni));
            }
        }
    }//GEN-LAST:event_jToggleButton1MousePressed


